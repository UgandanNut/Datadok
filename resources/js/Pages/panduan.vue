1. Masuk ke Akun Datadok Anda
Pertama, pastikan Anda sudah masuk ke akun Datadok Anda. Jika belum, buka Datadok dan lakukan login menggunakan kredensial yang sesuai.
2. Akses Menu Tim
Setelah berhasil masuk, navigasikan ke halaman Dashboard. Di sisi kiri layar, Anda akan melihat menu navigasi. Klik pada "Tim" untuk mengakses pengaturan tim bisnis Anda.
3. Pilih Opsi "Tambahkan Anggota"
Di halaman tim, cari tombol atau opsi "Tambahkan Anggota". Biasanya, tombol ini terletak di bagian atas atau bawah daftar anggota tim yang sudah ada.
4. Masukkan Detail Anggota Baru
Anda akan diarahkan ke formulir untuk menambahkan anggota baru. Masukkan informasi yang diperlukan, seperti:
Nama Lengkap: Nama anggota baru.
Alamat Email: Alamat email yang valid untuk mengundang anggota.
Peran: Pilih peran yang sesuai untuk anggota (misalnya, Admin, Anggota, atau Pengguna Terbatas).
5. Kirim Undangan
Setelah mengisi semua detail yang diperlukan, klik tombol "Kirim Undangan" atau "Tambahkan". Anggota baru akan menerima email undangan dengan petunjuk untuk bergabung dengan tim Anda di Datadok.
6. Konfirmasi Bergabung
Tunggu hingga anggota baru menerima dan mengonfirmasi undangan melalui email mereka. Setelah mereka menyetujui undangan, mereka akan ditambahkan secara resmi ke tim bisnis Anda.
7. Verifikasi dan Pengaturan Tambahan
Kembali ke halaman Tim untuk memverifikasi bahwa anggota baru telah ditambahkan dengan benar. Anda juga dapat mengatur izin dan peran tambahan jika diperlukan.
Tips Berguna
Periksa Email Spam: Jika undangan tidak diterima dalam waktu singkat, pastikan untuk memeriksa folder spam atau junk di email anggota baru.
Kustomisasi Peran: Sesuaikan peran dan izin anggota tim sesuai dengan kebutuhan dan tanggung jawab mereka.
